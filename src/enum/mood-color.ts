/**
 * 心情状态颜色
 *
 * 心情希望都是积极向上的，最差的就是灰色-中性的，希望各位使用的时候都有着开心的心情，避免糟糕的情绪
 */
export enum MoodColor {
  /**
   * 红色-兴奋的
   */
  RED = 'Excited',

  /**
   * 橙色-热情的
   */
  ORANGE = 'Enthusiastic',

  /**
   * 黄色-快乐的
   */
  YELLOW = 'Happy',

  /**
   * 绿色-放松的
   */
  GREEN = 'Relaxed',

  /**
   * 蓝色-平静的
   */
  BLUE = 'Calm',

  /**
   * 紫色-神秘的
   */
  PURPLE = 'Mysterious',

  /**
   * 灰色-中性的
   */
  GRAY = 'Neutral'
}
