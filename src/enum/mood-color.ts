/**
 * 心情状态颜色
 */
export enum MoodColor {
  /**
   * 开心
   */
  happy = '#FFD700', // 金黄色

  /**
   * 平静
   */
  calm = '#00FFFF', // 青色

  /**
   * 悲伤
   */
  sad = '#0000FF', // 蓝色

  /**
   * 生气
   */
  angry = '#FF0000', // 红色

  /**
   * 紧张
   */
  nervous = '#FFA500', // 橙色

  /**
   * 疲惫
   */
  tired = '#808080', // 灰色
}